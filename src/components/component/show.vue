  methods: {
    toggle() {
      var tab = document.getElementById("news-tab").getElementsByTagName("p");
      var show = document
        .getElementById("news-show")
        .getElementsByTagName("div");
      for (var i = 0; i < tab.length; i++) {
        tab[i].o = i;
        tab[i].onmouseover = function() {
          for (var n = 0; n < tab.length; n++) {
            show[n].className = "hide";
            tab[n].className = "";
          }
          show[this.o].className = "show";
          tab[this.o].className = "blue";
        };
      }
    }
  },
